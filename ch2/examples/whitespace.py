# “In programming, whitespace refers to any nonprinting characters,
# such as spaces, tabs, and end-of-line symbols”

print("Python")

# add a tab to your string
print("\tPython")

# add a newline to your string
print("Languages: \nPython\nJavaScript")

# you can combine these character combinations, e.g.
# use \n\t to tell Python to move to newline, THEN insert a tab
print("Languages:\n\tPython\n\tC\n\tJavaScript")